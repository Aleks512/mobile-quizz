                backgroundColor: const Color.fromARGB(255, 232, 55, 6),
              ),
              child: const Text(
                'False',
                style: TextStyle(
                  fontSize: 20.0,
                  color: Colors.white,
                ),
              ),
              onPressed: () {
                //The user picked true.

                bool correctAnswer = quizBrain.getAnwer(questionNumber);
                if (correctAnswer == false) {
                  print("OK");
                } else {
                  print("sorry,...");
                }
                setState(() {
                  questionNumber++;
                });
                print(questionNumber);
              },
            ),
          ),
        ),
        //TODO: Add a Row here as your score keeper
      ],
